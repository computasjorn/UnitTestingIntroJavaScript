class Mattulator {
  adder = (tall1, tall2) => {
    return tall1 + tall2;
  };

  subtraher = (tall1, tall2) => {
    return tall1 - tall2;
  };

  multipliser = (tall1, tall2) => {
    return tall1 * tall2;
  };

  divider = (teller, nevner) => {
    return nevner / teller;
  };
}

module.exports = Mattulator;
